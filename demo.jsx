/*
 * Display in a textarea the SVG code for a given icon
 */
let CodePreview = React.createClass({
  propTypes: {
    value: React.PropTypes.string.isRequired,
  },
  handleClick() {
    let node = React.findDOMNode(this);
    node.focus();
    node.select();
  },
  componentDidUpdate() {
    let node = React.findDOMNode(this);
    node.wrap = "off";
  },
  render() {
    return (
      <textarea
        spellCheck="false"
        readOnly="readonly"
        onClick={this.handleClick}
        value={this.props.value}
      />
    );
  }
});

/*
 * The collection of icons and the textarea
 */
let IconList = React.createClass({
  getInitialState: function() {
    return {
      selectedIcon: "my-icon",
      selectedCode: "<g>...</g>"
    };
  },
  handleClick(icon) {
    let code = React.renderToStaticMarkup(<Icon icon={icon} />);
    code = code.replace(/<svg .*><g>/, "<g>").replace("</svg>", "");
    this.setState({ selectedIcon: icon, selectedCode: code });
  },
  render() {
    let items = [];
    let code = `case '${this.state.selectedIcon}':\n  return (\n    ${this.state.selectedCode}\n  );`;

    Object.keys(icons).forEach((set) => {
      items[set] = icons[set].map((icon, index) => {
        return (
          <li key={index}>
            <IconButton onClick={this.handleClick.bind(this, icon)} icon={icon}/>
            <span className="icon-name">{icon.substring(0,27)}</span>
          </li>
        );
      });
    });

    return (
      <div>
        <h3>The Default Set</h3>
        <ul>{items.default}</ul>

        <h3>Av Icons</h3>
        <ul>{items.av}</ul>

        <h3>Communication Icons</h3>
        <ul>{items.communication}</ul>

        <h3>Device Icons</h3>
        <ul>{items.device}</ul>

        <h3>Editor Icons</h3>
        <ul>{items.editor}</ul>

        <h3>Hardware Icons</h3>
        <ul>{items.hardware}</ul>

        <h3>Image Icons</h3>
        <ul>{items.image}</ul>

        <h3>Maps Icons</h3>
        <ul>{items.maps}</ul>

        <h3>Notification Icons</h3>
        <ul>{items.notification}</ul>

        <h3>Social Icons</h3>
        <ul>{items.social}</ul>

        <div className="toolbar">
          <div className="toolbar--content">
            <CodePreview value={code}/>
          </div>
        </div>
      </div>
    );
  }
});

